_SPRITE_W_FULL = 16
_SPRITE_H_FULL = 16

def sprite_w():
    return _SPRITE_W_FULL // 2 if COMPACT else _SPRITE_W_FULL

def sprite_h():
    return _SPRITE_H_FULL // 2 if COMPACT else _SPRITE_H_FULL

# Backward-compat aliases used in rest-spot calculations (set in run())
SPRITE_W = _SPRITE_W_FULL
SPRITE_H = _SPRITE_H_FULL

class Cat:
    SIT='sit'; WALK='walk'; SLEEP='sleep'; LOAF='loaf'; HIDE='hide'

    def __init__(self, key, name, px, floor_py, cv_pw):
        self.key      = key
        self.name     = name
        self.d        = DEFS[key]
        self.sprites  = sprite_data.get_cat_sprites(key)
        self.px       = px
        self.py       = floor_py - sprite_h() + 4  # +4 compensates for transparent padding
        self.floor_py = floor_py
        self.cv_pw    = cv_pw
        self.dx       = random.choice([-1,1])
        self.state    = Cat.SIT
        self.frame    = 0
        self._frame_t = time.time()
        self._state_t = time.time() + random.uniform(3,8)
        # reminders
        self._water_t = time.time() + self.d['water_min']*60
        self._break_t = time.time() + self.d['break_min']*60
        self._motiv_t = time.time() + self.d['motiv_min']*60
        self._food_t  = time.time() + random.uniform(5, 15)*60  # first food check soon
        # bubble
        self.bubble   = f"Hi! I'm {name}! "
        self._bub_t   = time.time() + 5.0
        # special
        self.hidden   = False
        self._hide_t  = 0
        self.alive    = True
        # target-walking / rest-spot
        self.target_px    = None   # px to walk to
        self.target_py    = None   # py to settle at when arrived
        self.target_pose  = None   # state to enter when arrived
        self.claimed_spot = None   # index into scene.rest_spots, or None
        self._walk_fail_t = 0      # timestamp: give up walking if target unreachable
        # heart popup
        self.show_heart   = False
        self._heart_t     = 0
        # scene ref
        self._scene   = None

    def attach_scene(self, scene): self._scene = scene

    def say(self, text, dur=7.0):
        self.bubble  = text
        self._bub_t  = time.time() + dur

    def _msg(self, category):
        msgs = self.d['msg'].get(category, [])
        return random.choice(msgs) if msgs else ''

    def _release_spot(self):
        """Drop any claimed rest spot and return to floor level."""
        if self._scene and self.claimed_spot is not None:
            self._scene.release_spot(self)
            self.claimed_spot = None
            self.py = self.floor_py - sprite_h() + 4
        self.target_px = None
        self.target_py = None
        self.target_pose = None

    def _pick_state(self):
        # release any held spot first — cat is "moving on"
        self._release_spot()
        r = random.random()
        if r < 0.35:
            self.state = Cat.WALK
            self.dx = random.choice([-1,1])
            return
        # Try to claim a rest spot 60% of remaining → sit in bed / on tower / on keyboard (Mochi)
        if self._scene and r < 0.80:
            spot = self._scene.claim_spot(self)
            if spot:
                sp_px, sp_py, sp_pose, sp_idx = spot
                self.claimed_spot = sp_idx
                self.target_px    = sp_px
                self.target_py    = sp_py
                self.target_pose  = sp_pose
                self.state        = Cat.WALK
                self.dx           = 1 if sp_px > self.px else (-1 if sp_px < self.px else 1)
                self._walk_fail_t = time.time() + 20.0
                return
        # Fallback: just sit/loaf/sleep in place
        if   r < 0.88: self.state = Cat.SIT;   self.dx = 0
        elif r < 0.94: self.state = Cat.LOAF;  self.dx = 0
        else:          self.state = Cat.SLEEP; self.dx = 0

    def update(self):
        now = time.time()
        # animation frame
        rate = 0.22 if self.state == Cat.WALK else (2.0 if self.state == Cat.SLEEP else 1.5)
        if now - self._frame_t >= rate:
            self.frame += 1
            self._frame_t = now
        # clear bubble
        if self.bubble and now >= self._bub_t:
            self.bubble = None
        # clear heart
        if self.show_heart and now >= self._heart_t:
            self.show_heart = False
        # reminders (skip if bubble showing) — ring the terminal bell on wellness nudges
        if not self.bubble:
            if now >= self._water_t:
                self.say(self._msg('water'), 8); beep()
                self._water_t = now + self.d['water_min']*60
            elif now >= self._break_t:
                self.say(self._msg('stretch'), 8); beep()
                self._break_t = now + self.d['break_min']*60
            elif now >= self._motiv_t:
                self.say(self._msg('motiv'), 7)
                self._motiv_t = now + self.d['motiv_min']*60
            # Time-based food/snack reminders
            elif now >= self._food_t:
                hr = datetime.now().hour
                if hr in (7, 8, 12, 13, 18, 19):
                    m = self._msg('food_remind')
                    if m: self.say(m, 8); beep()
                elif 15 <= hr <= 17:
                    m = self._msg('snack_remind')
                    if m: self.say(m, 7); beep()
                self._food_t = now + 45*60  # check again in 45 min
        # special — Nyx hide/appear
        if self.key == 'nyx':
            if not self.hidden and random.random() < self.d.get('hide_chance',0):
                self.hidden = True
                self._hide_t = now + random.uniform(12, 35)
                self.say(self._msg('disappear'), 4)
            if self.hidden and now >= self._hide_t:
                self.hidden = False
                self.say(self._msg('appear'), 5)
        # special — Kulfi/Oreo knock plants
        for attr in ('knock_chance',):
            kc = self.d.get(attr, 0)
            if kc and random.random() < kc and self._scene:
                which = random.choice(['orchid','fern'])
                self._scene.knock(which)
                m = self._msg('knock')
                if m and not self.bubble:
                    self.say(m, 5)
        # special — Mochi/Oreo beg
        bc = self.d.get('beg_chance', 0)
        if bc and random.random() < bc and not self.bubble:
            cat = 'beg' if 'beg' in self.d['msg'] else 'attention_seek'
            m = self._msg(cat)
            if m: self.say(m, 6)
        # special — Saffron chirp
        cc = self.d.get('chirp_chance', 0)
        if cc and random.random() < cc and not self.bubble:
            m = self._msg('chirp')
            if m: self.say(m, 4)
        # state machine
        if now >= self._state_t:
            self._pick_state()
            self._state_t = now + random.uniform(3, 14)
        # movement
        if self.state == Cat.WALK:
            self.px += self.dx
            if self.target_px is not None:
                # Walking toward a rest spot: snap to pose when we arrive
                arrived = (
                    (self.dx > 0 and self.px >= self.target_px) or
                    (self.dx < 0 and self.px <= self.target_px) or
                    (self.dx == 0)
                )
                if arrived or now >= self._walk_fail_t:
                    if arrived:
                        self.px = self.target_px
                        self.py = self.target_py
                        self.state = self.target_pose or Cat.LOAF
                    else:
                        # Couldn't reach it in time — drop claim, resume wandering
                        self._release_spot()
                        self.state = Cat.SIT
                    self.dx = 0
                    self.target_px = None
                    self.target_py = None
                    self.target_pose = None
                    self._state_t = now + random.uniform(15, 35)
            else:
                lb = 22
                rb = self.cv_pw - sprite_w() - 4
                if self.px < lb: self.px = lb; self.dx = 1
                if self.px > rb: self.px = rb; self.dx = -1

    def sprite(self):
        if self.hidden:
            sp = sprite_data.get_nyx_eyes()
            return ds(sp, f'{self.key}_eyes') if sp else sp
        if self.state == Cat.WALK:
            k = 'walk_right' if self.dx >= 0 else 'walk_left'
        elif self.state == Cat.SLEEP:
            k = 'sleep_r'
        elif self.state == Cat.LOAF:
            k = 'sit2'
        else:
            k = 'sit'
        frames = self.sprites.get(k, self.sprites.get('sit', [[]]))
        frame = frames[self.frame % len(frames)] if frames else [[]]
        return ds(frame, f'{self.key}_{k}_{self.frame % max(1, len(frames))}')

    def hit(self, tc, tr):
        """Click hit test. tc/tr are 1-based terminal col/row."""
        cat_tc = self.px + 1
        cat_tr = self.py // 2 + 1
        sw, sh = sprite_w(), sprite_h()
        return (cat_tc <= tc <= cat_tc + sw and
                cat_tr  <= tr <= cat_tr  + sh // 2)

    def interact(self, action):
        msgs = self.d['msg']
        if action == 'pet':
            m = self._msg('pet')
            if self.key == 'hazel' and random.random() < self.d.get('flee_chance',0):
                self.say(self._msg('overwhelmed'), 5)
                time.sleep(0.05)
                self._release_spot()
                self.alive = False
                return
            self.show_heart = True
            self._heart_t = time.time() + 2.0
        elif action == 'cuddle':
            m = self._msg('cuddle') or self._msg('pet') or '...'
            self.show_heart = True
            self._heart_t = time.time() + 2.0
        elif action in msgs:
            m = self._msg(action)
        else:
            m = self._msg('food_love') or self._msg('eat') or '...'
        self.say(m or '...', 7)

