def bubble_lines(text, tc, tr, accent=(180,148,222)):
    """Return list of positioned ANSI strings for a speech bubble."""
    if not text: return []
    ac = fg(*accent)
    tc_c = fg(240,234,255)
    bg_c = bg(32,24,50)
    wrapped = textwrap.wrap(text, 26)
    if not wrapped: return []
    iw = max(len(l) for l in wrapped)
    h  = len(wrapped) + 3
    sr = max(1, tr - h - 1)
    sc = max(1, tc - iw//2 - 1)
    out = []
    out.append(f'\033[{sr};{sc}H{ac}╭{"─"*(iw+2)}╮{RST}')
    for i,ln in enumerate(wrapped):
        pad = ' '*(iw-len(ln))
        out.append(f'\033[{sr+1+i};{sc}H{ac}│{RST}{bg_c}{tc_c} {ln}{pad} {RST}{ac}│{RST}')
    er = sr+1+len(wrapped)
    out.append(f'\033[{er};{sc}H{ac}╰{"─"*(iw+2)}╯{RST}')
    out.append(f'\033[{er+1};{sc+iw//2+1}H{ac}╲{RST}')
    return out


def menu_box(title, options, sel, tw):
    """Pixel-style old-school menu. Returns (lines_list, menu_row, menu_col)."""
    W = 36
    ac = fg(185,145,225)
    bg_c = bg(26,20,40)
    title_c = fg(255,218,100)
    opt_c = fg(220,218,240)
    sel_bg = bg(155,115,218)
    sel_c  = fg(255,255,255)
    dim_c  = fg(118,108,148)

    def pad(s, w):
        return (s[:w] + ' '*w)[:w]

    lines = []
    lines.append(f'{ac}╔{"═"*W}╗{RST}')
    t = pad(f' >> {title}', W)
    lines.append(f'{ac}║{bg_c}{title_c}{BOLD}{t}{RST}{ac}║{RST}')
    lines.append(f'{ac}╠{"═"*W}╣{RST}')
    for i,(key,label) in enumerate(options):
        if i == sel:
            row = pad(f'  ▶  {label}', W)
            lines.append(f'{ac}║{sel_bg}{sel_c}{row}{RST}{ac}║{RST}')
        else:
            row = pad(f'     {label}', W)
            lines.append(f'{ac}║{bg_c}{opt_c}{row}{RST}{ac}║{RST}')
