    lines.append(f'{ac}║{bg_c}{dim_c}{pad("  ↑↓ or click  ·  Enter/click=pick  ·  Esc=close", W)}{RST}{ac}║{RST}')
    lines.append(f'{ac}╚{"═"*W}╝{RST}')
    return lines


