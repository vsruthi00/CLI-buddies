def save_state(cats):
    try:
        CFG_DIR.mkdir(parents=True, exist_ok=True)
        STATE_FILE.write_text(json.dumps({
            "cats": [{"key": c.key, "name": c.name} for c in cats]
        }, indent=2))
    except Exception:
        pass


def reset_state():
    try:
        if STATE_FILE.exists():
            STATE_FILE.unlink()
    except Exception:
        pass


def beep():
    """Terminal bell (BEL) — silent no-op if SOUND_ON is False."""
    if SOUND_ON:
        try:
            sys.stdout.write('\a')
            sys.stdout.flush()
        except Exception:
            pass


def _emergency_restore():
    """atexit hook: restore terminal if we crash hard without running cleanup."""
    try:
        sys.stdout.write('\033[?1000l\033[?1006l\033[?25h\033[0m')
        sys.stdout.flush()
    except Exception:
        pass

atexit.register(_emergency_restore)

