class Menu:
    NONE='none'; BELL='bell'; ACTION='action'; NAME='name'; DISMISS='dismiss'

    def __init__(self):
        self.mode   = Menu.NONE
        self.sel    = 0
        self.unsumm = list(ALL_CAT_KEYS)
        self.pending_key = None   # cat key being summoned
        self.pending_cat = None   # Cat obj being interacted with
        self.name_buf    = ''
        self._active_cats = []    # for dismiss menu

    def open_bell(self, active):
        used = {c.key for c in active}
        self.unsumm = [k for k in ALL_CAT_KEYS if k not in used]
        if not self.unsumm: return False
        self.mode = Menu.BELL; self.sel = 0; return True

    def open_action(self, cat):
        self.mode = Menu.ACTION; self.pending_cat = cat; self.sel = 0

    def open_name(self, key):
        self.mode = Menu.NAME; self.pending_key = key; self.name_buf = ''

    def open_dismiss(self, active):
        if not active: return False
        self._active_cats = list(active)
        self.mode = Menu.DISMISS; self.sel = 0; return True

    def dismiss_opts(self):
        return [(c.key, c.name) for c in self._active_cats]

    def close(self):
        self.mode = Menu.NONE; self.pending_key = None
        self.pending_cat = None; self.name_buf = ''
        self._active_cats = []

    def bell_opts(self):
        return [(k, DEFS[k]["label"]) for k in self.unsumm]

