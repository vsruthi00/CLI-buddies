# ── Runtime sprite downscaling ──────────────────────────────────────────────
# When the canvas is small (<=24 terminal rows), sprites are halved so the
# scene fits comfortably. Each 2×2 block is reduced to 1 pixel by picking the
# top-left non-transparent pixel.

COMPACT = False   # set True in run() when pane is small

def _downscale(sprite):
    """Halve a sprite: every 2×2 block -> 1 pixel (nearest-neighbor)."""
    if not sprite:
        return sprite
    h = len(sprite)
    w = len(sprite[0]) if sprite else 0
    out = []
    for y in range(0, h - 1, 2):
        row = []
        for x in range(0, w - 1, 2):
            # Pick first non-None pixel from the 2×2 block
            c = (sprite[y][x] or sprite[y][x+1]
                 or sprite[y+1][x] or sprite[y+1][x+1])
            row.append(c)
        out.append(row)
    return out

# Cache to avoid re-downscaling every frame
_ds_cache = {}

def ds(sprite, key=None):
    """Return downscaled sprite if COMPACT mode, else original."""
    if not COMPACT or not sprite:
        return sprite
    if key and key in _ds_cache:
        return _ds_cache[key]
    result = _downscale(sprite)
    if key:
        _ds_cache[key] = result
    return result

def ds_size(w, h):
    """Return downscaled dimensions if COMPACT mode."""
    if not COMPACT:
        return w, h
    return w // 2, h // 2

