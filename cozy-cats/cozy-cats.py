def run():
    import argparse
    ap = argparse.ArgumentParser(description='cozy-cats — terminal cat colony')
    ap.add_argument('--height', type=int, default=0,
                    help='Override pane height in terminal rows')
    ap.add_argument('--no-sound',   action='store_true',
                    help='Disable terminal-bell sound on reminders')
    ap.add_argument('--no-restore', action='store_true',
                    help='Do not re-summon cats from last session')
    ap.add_argument('--reset-state', action='store_true',
                    help='Delete saved colony state before launching')
    ap.add_argument('--smoke-test',  action='store_true',
                    help='Headless render test: no tty, just verify draw pipeline')
    ap.add_argument('--no-kitty',    action='store_true',
                    help='Force half-block rendering even in Kitty terminal')
    args = ap.parse_args()

    # Load + apply config BEFORE we build DEFS-dependent things
    write_default_config()
    cfg = load_config()
    apply_config(cfg)

    global SOUND_ON
    if args.no_sound:
        SOUND_ON = False
    if args.reset_state:
        reset_state()

    if args.smoke_test:
        run_smoke_test()
        return

    th, tw = term_size()
    ph = args.height if args.height > 0 else th

    # Rendering mode: Kitty graphics (scale=2) or half-block fallback (scale=1)
    # Kitty mode doubles the pixel grid in both directions, giving 4x the
    # pixels in the same terminal space. Sprites render at full resolution.
    use_kitty = is_kitty() and not args.no_kitty
    scale = 2 if use_kitty else 1

    # Only use compact/downscaled mode for half-block on small panes
    global COMPACT, SPRITE_W, SPRITE_H
    if scale == 1 and ph <= 24:
        COMPACT = True
        SPRITE_W = _SPRITE_W_FULL // 2
        SPRITE_H = _SPRITE_H_FULL // 2
        _ds_cache.clear()

    cv    = Canvas(ph, tw, scale=scale)
    scene = Scene(cv)
    cats  : list[Cat] = []
    menu  = Menu()

    # If the user passed --height smaller than the terminal, dock the scene
    # to the bottom of the terminal instead of the top. row_offset shifts
    # every positioned write down by N rows; mouse clicks get the reverse
    # translation before being handed to canvas-local hit tests.
    row_offset = max(0, th - ph)

    # Restore previously-summoned colony
    if not args.no_restore:
        for entry in load_state()[:4]:
            key  = entry.get('key')
            name = entry.get('name') or (DEFS.get(key, {}).get('label') if key else None)
            if not key or key not in DEFS:
                continue
            spx = random.randint(30, max(31, cv.pw - 20))
            c = Cat(key, name, spx, scene.floor_py, cv.pw)
            c.attach_scene(scene)
            cats.append(c)

    # Require a real TTY — we use raw mode + mouse protocol, neither of which
    # work under subprocess pipes, Claude Code's `!` prefix, or bare `python -c`.
    try:
        old_settings = termios.tcgetattr(sys.stdin)
    except termios.error:
        sys.stderr.write(
            "\ncozy-cats needs a real interactive terminal (TTY).\n"
            "It can't run under a pipe, subprocess launcher, or Claude Code's\n"
            "`!` prefix — those don't forward stdin as a TTY.\n\n"
            "Open a fresh terminal window (kitty, iTerm2, Terminal.app) and run:\n"
            "  python3 " + os.path.abspath(__file__) + "\n\n"
            "Or if you've sourced the shell shortcut:\n"
            "  cozy-cats\n\n"
            "For a headless render test that needs no TTY, use --smoke-test.\n"
        )
        sys.exit(1)

    def cleanup(sig=None, _=None):
        try: termios.tcsetattr(sys.stdin, termios.TCSADRAIN, old_settings)
        except: pass
        # Delete Kitty graphics image if we were using it
        kitty_del = '\033_Ga=d,i=1,q=2;\033\\' if use_kitty else ''
        sys.stdout.write(kitty_del + MOFF + SHOW + RST + '\033[2J\033[H')
        sys.stdout.flush()
        ac = fg(200,178,255)
        print(f'\n  {ac}Goodbye! Your cats miss you already~ {RST}\n')
        os._exit(0)

    signal.signal(signal.SIGINT,  cleanup)
    signal.signal(signal.SIGTERM, cleanup)

    try:
        tty.setraw(sys.stdin.fileno())
        sys.stdout.write(HIDE + MON)
        sys.stdout.flush()

        FPS    = CONFIGURED_FPS
        dt     = 1.0 / FPS
        last_f = 0.0
        prev_cat_count = len(cats)

        while True:
            now = time.time()

            # ── events ──────────────────────────────────────────────────────
            for evt in read_events():
                t = evt['t']

                if t == 'quit': cleanup()

                elif t == 'esc': menu.close()

                elif t == 'key':
                    k = evt['k']
                    if menu.mode in (Menu.BELL, Menu.ACTION, Menu.DISMISS):
                        if menu.mode == Menu.BELL:
                            opts_n = len(menu.unsumm)
                        elif menu.mode == Menu.ACTION:
                            opts_n = len(ACTION_OPTS)
                        else:
                            opts_n = len(menu._active_cats)
                        if k == 'UP':   menu.sel = (menu.sel-1) % opts_n
                        elif k == 'DOWN': menu.sel = (menu.sel+1) % opts_n
                        elif k == 'ENTER':
                            if menu.mode == Menu.BELL and menu.unsumm:
                                menu.open_name(menu.unsumm[menu.sel])
                            elif menu.mode == Menu.ACTION:
                                menu.pending_cat.interact(ACTION_KEYS[menu.sel])
                                menu.close()
                            elif menu.mode == Menu.DISMISS and menu._active_cats:
                                dismissed = menu._active_cats[menu.sel]
                                dismissed._release_spot()
                                dismissed.alive = False
                                menu.close()
                    elif menu.mode == Menu.NAME:
                        if k == 'ENTER':
                            name = menu.name_buf.strip() or DEFS[menu.pending_key]['label']
                            spx  = random.randint(30, max(31, cv.pw - 20))
                            c    = Cat(menu.pending_key, name, spx, scene.floor_py, cv.pw)
                            c.attach_scene(scene)
                            cats.append(c)
                            save_state(cats)
                            beep()
                            menu.close()
                        elif k == 'BACKSPACE':
                            menu.name_buf = menu.name_buf[:-1]
                        elif k not in ('UP','DOWN') and len(menu.name_buf) < 18:
                            menu.name_buf += k

                elif t == 'click':
                    tc = evt['col']
                    tr_raw = evt['row']
                    # Translate terminal click row into canvas-local row.
                    # Clicks above the docked scene are ignored (tr would be negative).
                    tr = tr_raw - row_offset
                    if tr < 1:
                        continue

                    # ── menu click ──────────────────────────────────────────
                    if menu.mode == Menu.BELL:
                        opts  = menu.bell_opts()
                        ml    = ph//2 - (len(opts)+6)//2
                        mc    = max(1, tw//2 - 19)
                        rel   = tr - ml - 2
                        if 0 <= rel < len(opts) and mc <= tc <= mc+38:
                            menu.sel = rel
                            menu.open_name(menu.unsumm[menu.sel])
                        elif tr < ml or tr > ml + len(opts) + 5:
                            menu.close()
                        continue

                    if menu.mode == Menu.ACTION:
                        ml  = ph//2 - (len(ACTION_OPTS)+6)//2
                        mc  = max(1, tw//2 - 19)
                        rel = tr - ml - 2
                        if 0 <= rel < len(ACTION_OPTS) and mc <= tc <= mc+38:
                            menu.sel = rel
                            menu.pending_cat.interact(ACTION_KEYS[menu.sel])
                            menu.close()
                        elif tr < ml or tr > ml + len(ACTION_OPTS) + 5:
                            menu.close()
                        continue

                    if menu.mode == Menu.DISMISS:
                        opts  = menu.dismiss_opts()
                        ml    = ph//2 - (len(opts)+6)//2
                        mc    = max(1, tw//2 - 19)
                        rel   = tr - ml - 2
                        if 0 <= rel < len(opts) and mc <= tc <= mc+38:
                            dismissed = menu._active_cats[rel]
                            dismissed._release_spot()
                            dismissed.alive = False
                            menu.close()
                        elif tr < ml or tr > ml + len(opts) + 5:
                            menu.close()
                        continue

                    if menu.mode == Menu.NAME:
                        menu.close(); continue

                    # ── scene clicks ────────────────────────────────────────
                    if scene.in_bell(tc, tr):
                        if len(cats) < 4:
                            menu.open_bell(cats)
                        continue

                    if scene.in_trash(tc, tr):
                        if cats:
                            menu.open_dismiss(cats)
                        continue

                    if scene.in_save(tc, tr):
                        save_state(cats)
                        # Brief confirmation via a temporary status message
                        for cat in cats:
                            if not cat.bubble:
                                cat.say("Saved!", 3)
                                break
                        continue

                    if scene.orchid_k and scene.in_orchid(tc, tr):
                        scene.fix('orchid'); continue
                    if scene.fern_k   and scene.in_fern(tc, tr):
                        scene.fix('fern');   continue

                    for cat in cats:
                        if cat.hit(tc, tr):
                            menu.open_action(cat); break

            # ── update cats ─────────────────────────────────────────────────
            for cat in cats:
                cat.update()
            cats = [c for c in cats if c.alive]
            if len(cats) != prev_cat_count:
                save_state(cats)
                prev_cat_count = len(cats)

            # ── render ──────────────────────────────────────────────────────
            if now - last_f < dt:
                time.sleep(0.01)
                continue
            last_f = now

            cv.fill()
            scene.draw()

            for cat in cats:
                sp = cat.sprite()
                py = cat.py if not cat.hidden else cat.py + 4
                cv.blit(py, cat.px, sp)
                # Heart popup above cat on pet/cuddle
                if cat.show_heart and not cat.hidden:
                    heart_sp = sprite_data.get_heart()
                    if heart_sp:
                        heart_sp = ds(heart_sp, 'heart')
                        cv.blit(py - 8, cat.px + sprite_w()//2 - 3, heart_sp)

            # When docked to the bottom, render draws into rows
            # (row_offset+1)..(row_offset+ph). Rows above stay as whatever
            # the shell had before — which is the intended dock behaviour.
            if use_kitty:
                out = [cv.render_kitty(start_row=1 + row_offset)]
            else:
                out = [cv.render(start_row=1 + row_offset)]

            # ── speech bubbles ───────────────────────────────────────────────
            for cat in cats:
                if cat.bubble and not cat.hidden:
                    tc = cat.px + sprite_w()//2 + 1
                    tr = cat.py//2 + 1 + row_offset
                    out.extend(bubble_lines(cat.bubble, tc, tr, (185,148,225)))

            # ── cat count ────────────────────────────────────────────────────
            cnt  = len(cats)

            # ── knocked-plant fix hints ──────────────────────────────────────
            hc = fg(255,200,80)
            if scene.orchid_k:
                ox = scene.plant1_px + 1
                oy = scene.plant1_py//2 + 1 + row_offset
                out.append(f'\033[{oy};{ox}H{hc}[click to fix plant]{RST}')
            if scene.fern_k:
                fx = scene.plant2_px + 1
                fy = scene.plant2_py//2 + 1 + row_offset
                out.append(f'\033[{fy};{fx}H{hc}[click to fix plant]{RST}')

            # ── menu overlays ────────────────────────────────────────────────
            if menu.mode == Menu.BELL:
                opts   = menu.bell_opts() or [('none','(all cats are here! max 4)')]
                mlines = menu_box('Summon a cat [BELL]', opts, menu.sel, tw)
                ml     = ph//2 - len(mlines)//2 + row_offset
                mc     = max(1, tw//2 - 19)
                for i,ln in enumerate(mlines):
                    out.append(f'\033[{ml+i};{mc}H{ln}')

            elif menu.mode == Menu.ACTION:
                cat_name = menu.pending_cat.name
                mlines   = menu_box(cat_name, ACTION_OPTS, menu.sel, tw)
                ml       = ph//2 - len(mlines)//2 + row_offset
                mc       = max(1, tw//2 - 19)
                for i,ln in enumerate(mlines):
                    out.append(f'\033[{ml+i};{mc}H{ln}')

            elif menu.mode == Menu.DISMISS:
                opts   = menu.dismiss_opts() or [('none','(no cats to dismiss)')]
                mlines = menu_box('Send away a cat', opts, menu.sel, tw)
                ml     = ph//2 - len(mlines)//2 + row_offset
                mc     = max(1, tw//2 - 19)
                for i,ln in enumerate(mlines):
                    out.append(f'\033[{ml+i};{mc}H{ln}')

            elif menu.mode == Menu.NAME:
                nlines = name_box(menu.pending_key, menu.name_buf, tw, ph//2 + row_offset)
                out.extend(nlines)

            # ── status bar ───────────────────────────────────────────────────
            hint = fg(95,82,125)
            hl   = fg(170,152,210)
            out.append(f'\033[{ph + row_offset};1H{hint}  {hl}BELL{hint}=summon  |  {hl}SAVE{hint}=save  |  {hl}DEL{hint}=dismiss  |  {hl}Ctrl+C{hint}=quit  |  {hl}{cnt}/4 cats{RST}')

            sys.stdout.write(''.join(out))
            sys.stdout.flush()

    except Exception as e:
        cleanup()
        raise


if __name__ == '__main__':
    run()
