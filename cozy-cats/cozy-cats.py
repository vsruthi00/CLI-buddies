# § 3  PIXEL CANVAS  (▀ half-block + Kitty graphics renderers)
# ══════════════════════════════════════════════════════════════════════════════

SCENE_BG = (138, 122, 158)  # lavender room colour
FLOOR_H  = 6              # base floor height in pixels (scaled in Kitty mode)

def is_kitty():
    """Detect Kitty terminal with graphics protocol support.
    Returns False inside tmux because tmux strips Kitty graphics sequences."""
    if os.environ.get('TMUX'):
        return False
    return os.environ.get('TERM_PROGRAM', '').lower() == 'kitty'


class Canvas:
    """
    Pixel framebuffer with two rendering modes:

    scale=1 (half-block): pw = term_w, ph = term_h * 2
        Each terminal cell shows 2 vertical pixels via the ▀ character.
        Resolution at 22 rows x 120 cols = 120 x 44 pixels.

    scale=2 (Kitty graphics): pw = term_w * 2, ph = term_h * 4
        Each terminal cell maps to a 2x4 pixel region. The Kitty
        graphics protocol renders the framebuffer as a native image,
        giving 4x the pixel count without character approximation.
        Resolution at 22 rows x 120 cols = 240 x 88 pixels.
    """
    def __init__(self, term_h, term_w, scale=1):
        self.th, self.tw = term_h, term_w
        self.scale = scale
        self.ph = term_h * 2 * scale   # pixel height
        self.pw = term_w * scale        # pixel width
        self._b = [[None]*self.pw for _ in range(self.ph)]

    def fill(self, color=None):
        c = color or SCENE_BG
        for row in self._b:
            for i in range(self.pw): row[i] = c

    def put(self, py, px, color):
        if 0 <= py < self.ph and 0 <= px < self.pw:
            self._b[py][px] = color

    def rect(self, py, px, h, w, color):
        for dy in range(h):
            for dx in range(w): self.put(py+dy, px+dx, color)

    def blit(self, py, px, sprite):
        """Stamp pixel sprite (list of rows of colour|None) at (py,px)."""
        for dr, row in enumerate(sprite):
            for dc, c in enumerate(row):
                if c is not None: self.put(py+dr, px+dc, c)

    def render(self, start_row=1):
        """Convert to ANSI string. Uses ▀ half-blocks (scale=1 fallback)."""
        SBG = SCENE_BG
        rows = self._b
        if self.ph % 2:
            rows = rows + [[None]*self.pw]
        parts = [HIDE]
        for i in range(0, len(rows), 2):
            tr = start_row + i // 2
            top_r, bot_r = rows[i], rows[i+1]
            parts.append(f'\033[{tr};1H')
            parts.append(bg(*SBG))
            for t, b in zip(top_r, bot_r):
                t = t or SBG
                b = b or SBG
                if t == b:
                    parts.append(bg(*t) + ' ')
                else:
                    parts.append(bg(*b) + fg(*t) + '▀')
            parts.append(RST)
        return ''.join(parts)

    def render_kitty(self, start_row=1):
        """Render canvas as a Kitty graphics protocol image.

        Sends zlib-compressed RGB data; Kitty scales it to fit the
        terminal cell grid (c x r cells). Much higher quality than
        half-block characters, especially for pixel art.
        """
        import base64, zlib
        SBG = SCENE_BG
        # Build RGB byte array
        buf = bytearray(self.ph * self.pw * 3)
        idx = 0
        for row in self._b:
            for c in row:
                r, g, b = c or SBG
                buf[idx] = r; buf[idx+1] = g; buf[idx+2] = b
                idx += 3

        compressed = zlib.compress(bytes(buf), 1)
        data = base64.standard_b64encode(compressed).decode('ascii')

        parts = [HIDE, f'\033[{start_row};1H']

        CHUNK = 4096
        header = f'a=T,f=24,o=z,s={self.pw},v={self.ph},c={self.tw},r={self.th},i=1,q=2'

        if len(data) <= CHUNK:
            parts.append(f'\033_G{header};{data}\033\\')
        else:
            chunks = [data[i:i+CHUNK] for i in range(0, len(data), CHUNK)]
            parts.append(f'\033_G{header},m=1;{chunks[0]}\033\\')
            for chunk in chunks[1:-1]:
                parts.append(f'\033_Gm=1;{chunk}\033\\')
            parts.append(f'\033_Gm=0;{chunks[-1]}\033\\')

        return ''.join(parts)

