def draw_background(cv):
    ph, pw = cv.ph, cv.pw
    s = cv.scale
    floor_h = FLOOR_H * s
    board_h = 2 * s
    floor_top = ph - floor_h
    for py in range(floor_top):
        t = py / max(1, floor_top)
        shade = tuple(int(WALL_HI[i] + (WALL_LO[i]-WALL_HI[i])*t) for i in range(3))
        for px in range(pw):
            cv.put(py, px, shade)
    for py in range(floor_top, ph):
        for px in range(pw):
            col = FL_A if ((px//6 + py//4) % 2 == 0) else FL_B
            cv.put(py, px, col)
    for py in range(floor_top, floor_top + board_h):
        for px in range(pw):
            cv.put(py, px, FL_BOARD)


def draw_sprite(cv, px, py, sprite_name, get_fn=None):
    """Blit a named sprite from sprite_data onto the canvas (downscaled if COMPACT)."""
    fn = get_fn or sprite_data.get_room_sprite
    sp = fn(sprite_name)
    if sp:
        sp = ds(sp, f'room_{sprite_name}')
        cv.blit(py, px, sp)


def draw_plant(cv, px, py, plant_name, knocked=False):
    """Draw a plant, optionally knocked over (shifted + scattered)."""
    sp = sprite_data.get_plant_sprite(plant_name)
    if not sp:
        return
    sp = ds(sp, f'plant_{plant_name}')
    if knocked:
        pw_p = len(sp[0]) if sp else 8
        ph_p = len(sp)
        for dr, row in enumerate(sp):
            for dc, c in enumerate(row):
                if c is not None:
                    cv.put(py + ph_p - 4 + dc % 6, px - 6 + dr, c)
        return
    cv.blit(py, px, sp)

