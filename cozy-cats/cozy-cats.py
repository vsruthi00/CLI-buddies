WALL_HI  = (148, 132, 168)
WALL_LO  = (128, 112, 148)
FL_A     = (138, 98, 62)
FL_B     = (124, 88, 55)
FL_BOARD = (108, 74, 46)

BELL_GOLD = (222, 172, 38)

